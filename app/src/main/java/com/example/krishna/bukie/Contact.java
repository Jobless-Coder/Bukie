package com.example.krishna.bukie;

public class Contact {
    String name;
    String phoneno;

    public Contact() {
    }

    public Contact(String name, String phoneno) {
        this.name = name;
        this.phoneno = phoneno;
    }

    public String getName() {
        return name;
    }

    public String getPhoneno() {
        return phoneno;
    }
}
